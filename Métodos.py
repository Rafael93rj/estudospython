#MÉTODOS EM ARRAYS
lista = [1, 2, 3, "p"]

print(lista.append("j")) # adiciona um novo elemento
print(lista.copy()) # copiar uma lista idêntica em outra instância
print(lista.count("j")) # conta quantas vezes um objeto aparece na lista
#print(lista.extend()) # junta duas listas
print(lista.index(3)) # procura a posição do obejto
print(lista.pop(3)) # remove o último objeto da lista
print(lista.remove(1)) # remove a partir da posição [0]
print(lista.reverse()) # inverte a sequência da lista
print(lista.sort()) # reordena a lista por ordem alfabética (caso seja uma lista de string)
print(len(lista)) # adiciona um novo elemento
print(lista.clear()) # exclui todos os elementos


